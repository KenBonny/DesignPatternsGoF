namespace Mediator
{
    class Program
    {
        static void Main(string[] args)
        {
            // a mediator decouples the communication between objects.
            // All communication passes through the mediator.
            // example: a thread resource manager that allows messages
            // to be sent from one thread to another.
            // example: chat program message distributor.
            // One speaker leaves a message for another member at the mediator
            // who will make sure the receiver will get the message.
            // https://en.wikipedia.org/wiki/Mediator_pattern
        }
    }
}
